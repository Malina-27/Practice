class Person:
    def __init__(self, name):
        self.name = name

p = Person("Ali")
print(p.name)
# Output:
# Ali



class Car:
    def __init__(self, brand):
        self.brand = brand

c = Car("BMW")
print(c.brand)
# Output:
# BMW



class Student:
    def __init__(self, name, age):
        self.name = name
        self.age = age

s = Student("Sara", 18)
print(s.age)
# Output:
# 18



class Dog:
    def __init__(self, color):
        self.color = color

d = Dog("Black")
print(d.color)
# Output:
# Black



class Book:
    def __init__(self, title):
        self.title = title

b = Book("Math")
print(b.title)
# Output:
# Math

