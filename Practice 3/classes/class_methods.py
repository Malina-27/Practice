class Person:
    def greet(self):
        print("Hello")

p = Person()
p.greet()
# Output:
# Hello



class Dog:
    def bark(self):
        print("Woof!")

d = Dog()
d.bark()
# Output:
# Woof!



class Student:
    def info(self, name):
        print("Student:", name)

s = Student()
s.info("Ali")
# Output:
# Student: Ali



class Car:
    def drive(self):
        print("Driving...")

c = Car()
c.drive()
# Output:
# Driving...



class Calculator:
    def add(self, a, b):
        print(a + b)

calc = Calculator()
calc.add(3, 4)
# Output:
# 7
