square = lambda x: x * x
print(square(4))
# Output:
# 16



add = lambda a, b: a + b
print(add(5, 2))
# Output:
# 7



multiply = lambda a, b: a * b
print(multiply(3, 4))
# Output:
# 12



greet = lambda name: "Hello " + name
print(greet("Ali"))
# Output:
# Hello Ali



is_even = lambda n: n % 2 == 0
print(is_even(7))
# Output:
# False
